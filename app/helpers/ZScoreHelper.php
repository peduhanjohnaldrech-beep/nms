    public static function computeWFA(float $weight, int $ageMonths, string $sex): ?float
    {
        $lms = self::getLMS($sex, $ageMonths, 'WFA');
        if (!$lms) return null;
        return self::lmsFormula($weight, $lms['l_value'], $lms['m_value'], $lms['s_value']);
    }

    public static function computeHFA(float $height, int $ageMonths, string $sex): ?float
    {
        $lms = self::getLMS($sex, $ageMonths, 'HFA');
        if (!$lms) return null;
        return self::lmsFormula($height, $lms['l_value'], $lms['m_value'], $lms['s_value']);
    }

    public static function classifyWFA(float $zscore): string
    {
        if ($zscore < -3.0) return 'SUW';
        if ($zscore < -2.0) return 'UW';
        if ($zscore <= 2.0) return 'Normal';
        if ($zscore <= 3.0) return 'OW';
        return 'OB';
    }

    public static function classifyHFA(float $zscore): string
    {
        if ($zscore < -3.0) return 'SSt';
        if ($zscore < -2.0) return 'St';
        return 'Normal';
    }

    public static function computeWFLH(float $weight, float $height, string $sex): ?array
    {
        $table = ($height < 87.0)
            ? self::getWFLTable($sex)
            : self::getWFHTable($sex);

        if (empty($table)) return null;

        $h    = (int) round($height);
        $keys = array_keys($table);
        $h    = max(min($keys), min(max($keys), $h));

        if (isset($table[$h])) {
            $sd = $table[$h];
        } else {
            $lower = $h;
            while ($lower >= min($keys) && !isset($table[$lower])) $lower--;
            $upper = $h;
            while ($upper <= max($keys) && !isset($table[$upper])) $upper++;
            if (!isset($table[$lower]) || !isset($table[$upper])) return null;
            $frac = ($h - $lower) / ($upper - $lower);
            $sd   = [];
            foreach (['n3', 'n2', 'med', 'p2', 'p3'] as $k) {
                $sd[$k] = $table[$lower][$k] + $frac * ($table[$upper][$k] - $table[$lower][$k]);
            }
        }

        return [
            'zscore' => round(self::approximateZFromSD($weight, $sd), 3),
            'status' => self::classifyWFLH_fromSD($weight, $sd),
        ];
    }

    private static function lmsFormula(float $x, float $L, float $M, float $S): float
    {
        if (abs($L) < 0.0001) return log($x / $M) / $S;
        return (pow($x / $M, $L) - 1) / ($L * $S);
    }

    private static function getLMS(string $sex, int $ageMonths, string $type): array|false
    {
        try {
            $stmt = Database::getInstance()->prepare(
                "SELECT l_value, m_value, s_value FROM who_growth_standards
                 WHERE sex = ? AND age_months = ? AND measurement_type = ?"
            );
            $stmt->execute([$sex, $ageMonths, $type]);
            return $stmt->fetch() ?: false;
        } catch (\Exception $e) {
            return false;
        }
    }

    private static function classifyWFLH_fromSD(float $weight, array $sd): string
    {
        if ($weight < $sd['n3']) return 'SW';
        if ($weight < $sd['n2']) return 'MW';
        if ($weight <= $sd['p2']) return 'Normal';
        if ($weight <= $sd['p3']) return 'OW';
        return 'OB';
    }

    private static function approximateZFromSD(float $weight, array $sd): float
    {
        if ($weight < $sd['n3']) {
            $diff = $sd['n3'] - $sd['n2'];
            return $diff > 0 ? -3.0 - ($sd['n3'] - $weight) / $diff : -4.0;
        }
        if ($weight < $sd['n2']) {
            $diff = $sd['n2'] - $sd['n3'];
            return $diff > 0 ? -3.0 + ($weight - $sd['n3']) / $diff : -3.0;
        }
        if ($weight <= $sd['med']) {
            $diff = $sd['med'] - $sd['n2'];
            return $diff > 0 ? -2.0 + 2.0 * ($weight - $sd['n2']) / $diff : -2.0;
        }
        if ($weight <= $sd['p2']) {
            $diff = $sd['p2'] - $sd['med'];
            return $diff > 0 ? 0.0 + 2.0 * ($weight - $sd['med']) / $diff : 0.0;
        }
        if ($weight <= $sd['p3']) {
            $diff = $sd['p3'] - $sd['p2'];
            return $diff > 0 ? 2.0 + ($weight - $sd['p2']) / $diff : 2.5;
        }
        $diff = $sd['p3'] - $sd['p2'];
        return $diff > 0 ? 3.0 + ($weight - $sd['p3']) / $diff : 4.0;
    }

